# My first Python program
print("Hello, I'm learning Python!")

# Variables
my_name = "Your Name"
my_age = 25

# Print them
print(f"My name is {my_name}")
print(f"I am {my_age} years old")

# Lists
favorite_colors = ["blue", "green", "red"]
print(f"I have {len(favorite_colors)} favorite colors")

# Loop
for color in favorite_colors:
    print(f"I like {color}")

# Function
def greet(name):
    return f"Hello, {name}!"

print(greet("World"))
